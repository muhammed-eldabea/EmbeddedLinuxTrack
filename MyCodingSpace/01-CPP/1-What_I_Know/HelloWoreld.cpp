#include <iostream>


int main() 
{   
    // std::cout is part of the C++ standard library 
    // we use the word 'std' to access the standard library
    // '::' is the scope resolution operator
    // 'endl' is used to insert a new line and flush the output buffer
    std::cout << "Hello, World!" << std::endl;
    return 0; 
}